-- admins
CREATE TABLE
IF NOT EXISTS admins
(
  id INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
  email VARCHAR
(255) NOT NULL UNIQUE,
  password VARCHAR
(255) NOT NULL,
  created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

